def decode_mode1_byte(byte):
    """Decode a BBC Micro Mode 1 byte into 4 pixel colour indices (0-3)."""
    pixels = []
    for i in range(4):
        bit_hi = (byte >> (7 - i)) & 1
        bit_lo = (byte >> (3 - i)) & 1
        pixels.append((bit_hi << 1) | bit_lo)
    return pixels


def decode_sprite(obj_type):
    """Decode a sprite's A/B streams into a 2D array of colour indices.

    The A stream encodes position: bytes >= 0x80 advance to the next pixel row
    and provide a column offset (byte & 0x7F); bytes < 0x80 are column offsets
    within the current row.  0xFF terminates.

    The B stream provides pixel data bytes consumed in lockstep (same index).

    Returns an array of shape (rows, cols*4) with values 0-3.
    """
    data = SPRITE_DATA[obj_type]
    a_stream = data["A"]
    b_stream = data["B"]
    width_px = data["w"] * 4

    # First pass: determine number of rows
    row = 0
    for a_val in a_stream:
        if a_val == 0xFF:
            break
        if a_val >= 0x80:
            row += 1
    # Row 0 is always used (either plotted directly or skipped past by first advance)
    num_rows = row + 1

    pixels = np.zeros((num_rows, width_px), dtype=int)
    row = 0
    idx = 0

    while idx < len(a_stream) and idx < len(b_stream):
        a_val = a_stream[idx]
        if a_val == 0xFF:
            break

        if a_val >= 0x80:
            # Advance to next pixel row, then plot
            row += 1
            col_byte = (a_val & 0x7F) // 8
        else:
            # Plot on current row
            col_byte = a_val // 8

        b_val = b_stream[idx]
        px = decode_mode1_byte(b_val)
        col_px = col_byte * 4
        if 0 <= row < num_rows and col_px + 4 <= width_px:
            pixels[row, col_px:col_px + 4] = px

        idx += 1

    return pixels


def sprite_to_rgba(pixel_array, level_num):
    """Convert a sprite colour-index array to RGBA using the level's palette.

    Colour 0 = transparent (background)
    Colour 1 = yellow (ship/highlight)
    Colour 2 = landscape colour
    Colour 3 = object colour
    """
    palette_rgb = {
        0: (0, 0, 0, 0),           # transparent
        1: (255, 255, 0, 255),      # yellow (always)
        2: _hex_to_rgba(BBC_COLOURS[LEVEL_LANDSCAPE_COLOUR[level_num]]),
        3: _hex_to_rgba(BBC_COLOURS[LEVEL_OBJECT_COLOUR[level_num]]),
    }
    h, w = pixel_array.shape
    rgba = np.zeros((h, w, 4), dtype=np.uint8)
    for c, colour in palette_rgb.items():
        mask = pixel_array == c
        rgba[mask] = colour
    return rgba


def _hex_to_rgba(hex_colour):
    """Convert '#RRGGBB' to (R, G, B, 255)."""
    r = int(hex_colour[1:3], 16)
    g = int(hex_colour[3:5], 16)
    b = int(hex_colour[5:7], 16)
    return (r, g, b, 255)


